// Variables are used to store data which can be acquired and also changed later.

var firstName = "Himadri";
var secondName = "Mondal";

console.log(firstName);
console.log(secondName);

console.log(firstName + " " + secondName);
// We can add several variables together using '+' symbol and print them together. (just like in Python)

var firstName = "Rakhi";
// We can write var to state the type of data when modifying the variable value.

console.log(firstName + " " + secondName);

firstName = "Rudra";
// Or we may not state the type of data being given in replacement of the previous alloted value to the variable.
// In this case the type of data alloted to the first value will also be alloted to the second value.

var middleName = "Raj";

console.log(firstName + " " + middleName + " " + secondName);
